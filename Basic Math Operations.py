# Addition
result = 10 + 5
print(result)  # Output: 15

# Subtraction
result = 10 - 5
print(result)  # Output: 5

# Multiplication
result = 10 * 5
print(result)  # Output: 50

# Division
result = 10 / 5
print(result)  # Output: 2.0 (Note: In Python 3, division always returns a float)

# Exponentiation
result = 10 ** 2
print(result)  # Output: 100
