# Strings are sequences of characters
# Python provides many operations and functions to manipulate them.

# String assignment
name = "Alice"
greeting = "Hello, "

# Concatenation
message = greeting + name
print(message)  # Output: Hello, Alice

# String length
length = len(name)
print(length)   # Output: 5

# Accessing characters
first_char = name[0]    # Indexing starts at 0
print(first_char)       # Output: A

# Slicing
substring = name[1:3]   # Slice from index 1 to index 2 (exclusive)
print(substring)        # Output: li
